//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Account::getGroups(I_GroupVisitor& _visitor) const
{
    _visitor.begin();

    Groups_type::const_iterator iter = m_groups.begin();
    while( iter != m_groups.end() )
    {
        _visitor.visit(**iter);
        iter++;
    }

    _visitor.end();
}

