//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
GroupController::GroupController(AccountService& _service, Common::I_GroupModel& _model)
:   m_service(_service)
,   m_model(*dynamic_cast<GroupModel*>(&_model))
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
GroupController::~GroupController()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Common::I_AccountService& 
GroupController::getAccountService() const
{
    return m_service;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
GroupController::pFutureGroup_type 
GroupController::createGroup(Common::I_Session& _session, const std::string& _name, const std::string& _description)
{
    return getModel().createGroup(_session,_name,_description);    
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
GroupController::pFutureGroup_type 
GroupController::getGroup(Common::I_Session& _session, const std::string& _name)
{
    return getModel().getGroup(_session,_name);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
GroupController::updateGroup(Common::I_Session& _session, Common::I_Group& _group)
{
    getModel().updateGroup(_session, _group);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
GroupController::removeGroup(Common::I_Session& _session, Common::I_Group& _group)
{
    getModel().removeGroup(_session, _group);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void 
GroupController::getGroups(Common::I_Session& _session, I_GroupVisitor& _visitor)
{
    getModel().getGroups(_session,_visitor);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
GroupModel&
GroupController::getModel() const
{
    return m_model;
}

