//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Group::getGroupMembers(I_AccountVisitor& _visitor) const
{
    /// TODO I was actually thinking about using a subscribed
    /// GroupView with a filter instead of a collection for this.
    _visitor.begin();

    Accounts_type::const_iterator iter = m_accounts.begin();
    while( iter != m_accounts.end() )
    {
        _visitor.visit(**iter);
        iter++;
    }

    _visitor.end();
}

