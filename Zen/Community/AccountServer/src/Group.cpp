//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static Zen::Scripting::script_module* sm_pScriptModule = NULL;
static std::string sm_scriptTypeName("Group");
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Group::registerScriptModule(Zen::Scripting::script_module& _module)
{
    sm_pScriptModule = &_module;

    sm_pScriptModule->addType<Common::I_Group>(sm_scriptTypeName, "Group")
        .addMethod("getGroupName", &Common::I_Group::getGroupName)
        .addMethod("getGroupDescription", &Common::I_Group::getGroupDescription)
    ;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string&
Group::getScriptTypeName()
{
    return sm_scriptTypeName;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Zen::Scripting::I_ObjectReference*
Group::getScriptObject()
{
    if (m_pScriptObject == NULL)
    {
        m_pScriptObject = new ScriptWrapper_type(sm_pScriptModule->getScriptModule(),
            sm_pScriptModule->getScriptModule()->getScriptType(getScriptTypeName()),
            getSelfReference().lock()
        );
    }

    return m_pScriptObject;
}

