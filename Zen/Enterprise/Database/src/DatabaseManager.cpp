//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
I_DatabaseManager::pDatabaseService_type
DatabaseManager::createDatabaseService(const std::string& _type, config_type& _config)
{
    Threading::CriticalSection guard(m_databaseServiceCache.getLock());

    pDatabaseService_type pService(m_databaseServiceCache.getCachedService(_type));

    if (pService.isValid())
    {
        return pService;
    }

    I_DatabaseServiceFactory* pFactory = m_databaseServiceCache.getFactory(_type);

    if (pFactory == NULL)
    {
        // TODO Error
        return pService;
    }

    return m_databaseServiceCache.cacheService(_type, pFactory->create(_type, _config));

}

