{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ClampRotationBehavior::~ClampRotationBehavior()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool
ClampRotationBehavior::handlePhysicsTransform(Engine::Core::I_BaseGameObject& _gameObject, Engine::Physics::I_PhysicsShape::I_TransformEventData& _eventData)
{
    Math::Matrix4 orientation;
    _eventData.getShape().getOrientation(orientation);

    Math::Matrix4 newOrientation(Math::Matrix4::INIT_IDENTITY);
    Math::Point3 position;
    orientation.getPosition(position);
    newOrientation.setPosition(position);

    _eventData.getShape().setOrientation(newOrientation);

    Math::Vector3 stopped(0.0f, 0.0f, 0.0f);
    _eventData.getShape().setAngularMomentum(stopped);
    return true;
}

