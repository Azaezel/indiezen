//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
KeyState::KeyState(Engine::Input::KeyCode_enum _code,
                   const Engine::Input::I_KeyModifierState* _pState)
:   m_code(_code)
,   m_pModifierState(_pState)
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
KeyState::KeyState(const KeyState& _keyState)
:   m_code(_keyState.m_code)
,   m_pModifierState(_keyState.m_pModifierState)
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
KeyState::KeyState(const I_KeyState& _keyState)
:   m_code(_keyState.getKeyCode())
,   m_pModifierState(&_keyState.getModifierState())
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
KeyState::~KeyState()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
KeyState&
KeyState::operator=(const KeyState& _keyState)
{
    m_code = _keyState.getKeyCode();
    m_pModifierState = &_keyState.getModifierState();
    return *this;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Engine::Input::KeyCode_enum 
KeyState::getKeyCode() const
{
    return m_code;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const Engine::Input::I_KeyModifierState& 
KeyState::getModifierState() const
{
    return *m_pModifierState;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool 
KeyState::operator==(Engine::Input::KeyCode_enum _rhs) const
{
    return (m_code == _rhs);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool 
KeyState::operator==(const Engine::Input::I_KeyModifierState& _rhs) const
{
    return (*m_pModifierState == _rhs);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool 
KeyState::operator|=(const Engine::Input::I_KeyModifierState& _rhs) const
{
    return (*m_pModifierState |= _rhs);
}

