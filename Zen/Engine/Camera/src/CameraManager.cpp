//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
I_CameraManager::pCameraService_type
CameraManager::create(const std::string& _type)
{
    Threading::CriticalSection guard(m_cameraServiceCache.getLock());

    pCameraService_type pService(m_cameraServiceCache.getCachedService(_type));

    if (pService.isValid())
    {
        return pService;
    }

    I_CameraServiceFactory* pFactory = m_cameraServiceCache.getFactory(_type);

    if (pFactory == NULL)
    {
        // TODO Error
        return pService;
    }

#if 0 // do this to add scripting support
    if (m_pDefaultScriptEngine != NULL)
    {
        registerScriptEngine(m_pDefaultScriptEngine, pService);
    }
#endif

    // Connect CameraManager::onFrame() to the CameraService onFrameEvent
    //pService->onFrameEvent.connect(boost::bind(&CameraManager::onFrame, this, _1));

    return m_cameraServiceCache.cacheService(_type, pFactory->create(_type));
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
CameraManager::onFrame(frameDelta_type frameDelta)
{
    // Fire the CameraManager::onFrameEvent event
    this->onFrameEvent(frameDelta);
}

