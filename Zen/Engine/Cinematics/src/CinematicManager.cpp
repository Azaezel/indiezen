//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
I_CinematicManager::pCinematicService_type
CinematicManager::createCinematicService(const std::string& _type)
{
    Threading::CriticalSection guard(m_cinematicServiceCache.getLock());

    pCinematicService_type pService(m_cinematicServiceCache.getCachedService(_type));

    if (pService.isValid())
    {
        return pService;
    }

    I_CinematicServiceFactory* pFactory = m_cinematicServiceCache.getFactory(_type);

    if (pFactory == NULL)
    {
        // TODO Error
        return pService;
    }

    return m_cinematicServiceCache.cacheService(_type, pFactory->createCinematicService(_type));

}

