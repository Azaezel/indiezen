//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
NavigationManager::pService_type
NavigationManager::create(const std::string& _type)
{
    Threading::CriticalSection guard(m_serviceCache.getLock());

    pService_type pService(m_serviceCache.getCachedService(_type));

    if (pService.isValid())
    {
        return pService;
    }

    I_NavigationServiceFactory* pFactory = m_serviceCache.getFactory(_type);

    if (pFactory == NULL)
    {
        std::stringstream errorMessage;
        errorMessage << "Zen::Navigation::NavigationManager::create() : Error: could not create navigation service factory for type " << _type;

        throw Utility::runtime_exception(errorMessage.str());
    }

#if 0 // scripting not supported yet
    if (m_pDefaultScriptEngine.isValid())
    {
        registerScriptEngine(m_pDefaultScriptEngine, pService);
    }
#endif

    return m_serviceCache.cacheService(_type, pFactory->create(_type));
}

