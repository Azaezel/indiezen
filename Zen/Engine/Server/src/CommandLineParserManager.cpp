//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
CommandLineParserManager::CommandLineParserManager()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
CommandLineParserManager::~CommandLineParserManager()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
CommandLineParserManager::pService_type
CommandLineParserManager::create(const std::string& _type)
{
    Threading::CriticalSection guard(this->m_serviceCache.getLock());

    pService_type pService(this->m_serviceCache.getCachedService(_type));

    if(pService.isValid())
    {
        return pService;
    }

    I_CommandLineParserServiceFactory* pFactory = m_serviceCache.getFactory(_type);

    if( pFactory == NULL )
    {
        throw Utility::runtime_exception("CommandLineParserManager::create(): Error, could not get factory.");
    }

    return this->m_serviceCache.cacheService( _type, pFactory->create(_type) );
}

