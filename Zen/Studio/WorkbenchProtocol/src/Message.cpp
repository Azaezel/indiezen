//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Message::pMessageFactory_type Message::sm_pMessageFactory;
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Message::Message(pMessageHeader_type _pMessageHeader,
                 pEndpoint_type _pSourceEndpoint,
                 pEndpoint_type _pDestinationEndpoint)
:   m_pMessageHeader(_pMessageHeader)
,   m_pSourceEndpoint(_pSourceEndpoint)
,   m_pDestinationEndpoint(_pDestinationEndpoint)
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Message::~Message()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Message::pEndpoint_type
Message::getSourceEndpoint()
{
    return m_pSourceEndpoint;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Message::pEndpoint_type
Message::getDestinationEndpoint()
{
    return m_pDestinationEndpoint;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Message::pMessageHeader_type
Message::getMessageHeader() const
{
    return m_pMessageHeader;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
unsigned int 
Message::getMessageId() const
{
    static Zen::Threading::SpinLock sm_spinLock;
    static unsigned int sm_lastId = 0;

    Zen::Threading::xCriticalSection lock(sm_spinLock);
    return ++sm_lastId;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Message::destroyMessageFactory(wpMessageFactory_type _wpMessageFactory)
{
    MessageFactory* pFactory = dynamic_cast<MessageFactory*>(_wpMessageFactory.get());

    if( pFactory != NULL )
    {
        delete pFactory;
    }
    else
    {
        throw Zen::Utility::runtime_exception("LoginRequest::destroyMessageFactory() : Invalid type.");
    }
}

