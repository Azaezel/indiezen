//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ProjectExplorerNode::ProjectExplorerNode(ProjectExplorerModel& _model, pUserData_type _pUserData, I_Project* _pProject, ProjectExplorerNode* _pParent)
:   m_model(_model)
,   m_pUserData(_pUserData)
,   m_pProject(_pProject)
,   m_pParent(_pParent)
{
    m_pUserData->setNode(this);
    if (m_pParent != NULL)
    {
        m_pParent->addChild(this);
    }

    // Connect the onModified event to the model.
    m_pEventConnection =
        onModified.connect(boost::bind(&ProjectExplorerModel::onNodeModified, &_model, _1));
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ProjectExplorerNode::~ProjectExplorerNode()
{
    if (m_pParent != NULL)
    {
        m_pParent->removeChild(this);
    }

    // Disconnect the onModified event.
    m_pEventConnection->disconnect();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string&
ProjectExplorerNode::getDisplayName() const
{
    if (m_pUserData.isValid())
    {
        return m_pUserData->getName();
    }
    else
    {
        // This *should* be dead code since m_pUserData should always be valid.
        return m_displayName;
    }
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
ProjectExplorerNode::setDisplayName(const std::string& _name)
{
    if (m_pUserData.isValid())
    {
        return m_pUserData->setName(_name);
        onModified(this);
    }
    else
    {
        // This *should* be dead code since m_pUserData should always be valid.
        m_displayName = _name;
    }
}

