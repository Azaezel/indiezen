//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
ProjectExplorerNode::getChildren(I_ExplorerNodeVisitor& _visitor)
{
    // TODO Guard

    _visitor.begin();

    for(Children_type::iterator iter = m_children.begin(); iter != m_children.end(); iter++)
    {
        _visitor.visit(**iter);
    }

    _visitor.end();
}

