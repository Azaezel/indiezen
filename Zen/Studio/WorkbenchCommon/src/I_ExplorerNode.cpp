//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
I_ExplorerNode::UserData::setName(const std::string &_name)
{
    m_name = _name;

    // Check to make sure the node isn't NULL.
    // The only time it will be NULL is during initialization and onModified()
    // does not need to be fired (because it's being initialized not modified).
    if (getNode() != NULL)
    {
        getNode()->onModified(m_pNode);
    }
}

