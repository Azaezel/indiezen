//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ProjectServiceManager::pProjectService_type
ProjectServiceManager::createProjectService(const I_ProjectType& _projectType, wpWorkbench_type _pWorkbench)
{
    Threading::CriticalSection guard(m_projectServiceCache.getLock());

    pProjectService_type pService(m_projectServiceCache.getCachedService(_projectType.getType()));

    if( pService.isValid() )
    {
        return pService;
    }

    I_ProjectServiceFactory* pFactory = m_projectServiceCache.getFactory(_projectType.getType());

    if( pFactory == NULL )
    {
        // TODO: Error
        return pService;
    }

    return m_projectServiceCache.cacheService(_projectType.getType(), pFactory->create());
}

