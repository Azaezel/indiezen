Project::Project(Zen::Studio::Workbench::I_ProjectExplorerController& _controller, const std::string& _name)
:   I_Project(_name)
,   m_controller(_controller)
{
    m_projectPath =
        getWorkbenchService().getWorkspacePath() / _name;

    // Double-check to make sure the path exists
    if (!boost::filesystem::exists(m_projectPath))
    {
        boost::filesystem::create_directories(m_projectPath);
    }

    m_controlPath = getWorkbenchService().getWorkspacePath() / _name / ".workbench";

    if (!boost::filesystem::exists(m_controlPath))
    {
        boost::filesystem::create_directories(m_controlPath);
    }

    // TODO Make the project database
}

