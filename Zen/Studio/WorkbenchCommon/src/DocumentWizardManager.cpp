//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
I_DocumentWizardManager::pDocumentWizardService_type
DocumentWizardManager::create(const std::string& _type)
{
  Threading::CriticalSection guard(m_serviceCache.getLock());
  
  pDocumentWizardService_type pService(m_serviceCache.getCachedService(_type));

  if( pService.isValid() )
  {
	return pService;
  }

  I_DocumentWizardFactory* pFactory = m_serviceCache.getFactory(_type);

  if( pFactory == NULL )
  {
	//TODO: Error
	return pService;
  }

  return m_serviceCache.cacheService( _type, pFactory->create(_type));
  
}

