#include "BehaviorGroup.hpp"
#include "BehaviorManager.hpp"
#include "GameObject.hpp"

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
namespace Game {
namespace Client {
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
BehaviorGroup::BehaviorGroup(BehaviorManager& _manager, const std::string& _groupName)
:   m_manager(_manager)
,   m_pBehaviors(m_manager.game().getBehaviorService().createBehaviors(_groupName))
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
BehaviorGroup::~BehaviorGroup()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
BehaviorGroup&
BehaviorGroup::add(const std::string& _behaviorName)
{
    m_manager.add(m_pBehaviors, _behaviorName);

    return *this;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
BehaviorGroup&
BehaviorGroup::attach(GameObject& _gameObject)
{
    _gameObject.base().setBehaviors(m_pBehaviors);
    return *this;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
}   // namespace Client
}   // namespace Game
