//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
ContributorServiceFactory::destroy(wpContributorService_type _pService)
{
    ContributorService* pService = dynamic_cast<ContributorService*>(_pService.get());

    if (pService)
    {
        delete pService;
    }
    else
    {
        // TODO Error?
    }
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ContributorServiceFactory::pContributorService_type
ContributorServiceFactory::create(const std::string& _type, Zen::Studio::Workbench::I_Workbench& _workbench)
{
    ContributorService* pRawActionService = new ContributorService(_workbench);
    return pContributorService_type(pRawActionService, &ContributorServiceFactory::destroy);
}

