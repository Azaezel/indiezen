//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
I_AuthenticateResponse::pResponse_type
I_AuthenticateResponse::create(pEndpoint_type _pSourceEndpoint,
							   pEndpoint_type _pDestinationEndpoint,
							   pResourceLocation_type _pSrcLocation,
							   pResourceLocation_type _pDestLocation,
							   boost::uint64_t _requestMessageId)
{
    AuthenticateResponse* pAuthenticateResponse(
        new AuthenticateResponse(
             _pSourceEndpoint,
             _pDestinationEndpoint,
             _pSrcLocation,
             _pDestLocation,
            _requestMessageId
        )
    );

    return pResponse_type(
        (Response*)pAuthenticateResponse,
        AuthenticateResponse::destroy
    );
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
I_AuthenticateResponse::pMessageType_type
I_AuthenticateResponse::getStaticMessageType()
{
    return AuthenticateResponse::sm_pType;
}

