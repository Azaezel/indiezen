void
Account::serialize(boost::archive::polymorphic_oarchive& _archive, const int _version)
{
    _archive & m_accountId;
    _archive & m_userId;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static Zen::Scripting::script_module* sm_pScriptModule = NULL;
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Account::registerScriptModule(Zen::Scripting::script_module& _module)
{
    sm_pScriptModule = &_module;

    sm_pScriptModule->addType<Account>("Account", "Account")
    ;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Zen::Scripting::I_ObjectReference*
Account::getScriptObject()
{
    if (m_pScriptObject == NULL)
    {
        m_pScriptObject = new ScriptWrapper_type(sm_pScriptModule->getScriptModule(),
            sm_pScriptModule->getScriptModule()->getScriptType(getScriptTypeName()),
            getSelfReference().lock()
        );
    }

    return m_pScriptObject;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool
Account::NativeAccountId::operator==(const AccountId::I_NativeAccountId& _otherId) const
{
    const NativeAccountId* const pNativeAccountId = dynamic_cast<const NativeAccountId*>(&_otherId);
    return (pNativeAccountId != NULL) && (m_nativeAccountId == pNativeAccountId->m_nativeAccountId);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool
Account::NativeAccountId::operator!=(const AccountId::I_NativeAccountId& _otherId) const
{
    const NativeAccountId* const pNativeAccountId = dynamic_cast<const NativeAccountId*>(&_otherId);
    return (pNativeAccountId == NULL) && (m_nativeAccountId != pNativeAccountId->m_nativeAccountId);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool
Account::NativeAccountId::operator <(const AccountId::I_NativeAccountId& _otherId) const
{
    const NativeAccountId* const pNativeAccountId = dynamic_cast<const NativeAccountId*>(&_otherId);
    assert(pNativeAccountId != NULL);
    return (m_nativeAccountId < pNativeAccountId->m_nativeAccountId);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Account::AccountId::I_NativeAccountId*
Account::NativeAccountId::clone() const
{
    return new Account::NativeAccountId(m_nativeAccountId);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
std::string
Account::NativeAccountId::toString() const
{
    std::ostringstream oStream;
    oStream << m_nativeAccountId;
    return oStream.str();
}

