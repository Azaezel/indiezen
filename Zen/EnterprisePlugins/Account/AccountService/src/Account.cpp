//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Account::Account()
:   m_accountId(new NativeAccountId())
,   m_pScriptObject(NULL)
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Account::Account(const Account& _account)
:   m_accountId(_account.m_accountId)
,   m_pScriptObject(NULL)
,   I_Account(_account)
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Account::Account(pAccountDomainObject_type _pDomainObject)
:   m_accountId(new NativeAccountId(_pDomainObject->getAccountId().getInt64Value()))
,   m_userId(_pDomainObject->getUserId().getStringValue())
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Account::Account(boost::uint64_t _nativeAccountId)
:   m_accountId(new NativeAccountId(_nativeAccountId))
,	m_pScriptObject(NULL)
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Account::~Account()
{
}

