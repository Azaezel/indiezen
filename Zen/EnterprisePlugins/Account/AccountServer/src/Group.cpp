void
Group::serialize(boost::archive::polymorphic_oarchive& _archive, const int _version)
{
    _archive & m_groupId;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static Zen::Scripting::script_module* sm_pScriptModule = NULL;
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Group::registerScriptModule(Zen::Scripting::script_module& _module)
{
    sm_pScriptModule = &_module;

    sm_pScriptModule->addType<Group>("Group", "Group")
    ;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Zen::Scripting::I_ObjectReference*
Group::getScriptObject()
{
    if (m_pScriptObject == NULL)
    {
        m_pScriptObject = new ScriptWrapper_type(sm_pScriptModule->getScriptModule(),
            sm_pScriptModule->getScriptModule()->getScriptType(getScriptTypeName()),
            getSelfReference().lock()
        );
    }

    return m_pScriptObject;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool
Group::NativeGroupId::operator==(const GroupId::I_NativeGroupId& _otherId) const
{
    const NativeGroupId* const pNativeGroupId = dynamic_cast<const NativeGroupId*>(&_otherId);
    return (pNativeGroupId != NULL) && (m_nativeGroupId == pNativeGroupId->m_nativeGroupId);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool
Group::NativeGroupId::operator!=(const GroupId::I_NativeGroupId& _otherId) const
{
    const NativeGroupId* const pNativeGroupId = dynamic_cast<const NativeGroupId*>(&_otherId);
    return (pNativeGroupId == NULL) && (m_nativeGroupId != pNativeGroupId->m_nativeGroupId);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool
Group::NativeGroupId::operator <(const GroupId::I_NativeGroupId& _otherId) const
{
    const NativeGroupId* const pNativeGroupId = dynamic_cast<const NativeGroupId*>(&_otherId);
    assert(pNativeGroupId != NULL);
    return (m_nativeGroupId < pNativeGroupId->m_nativeGroupId);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Group::GroupId::I_NativeGroupId*
Group::NativeGroupId::clone() const
{
    return new Group::NativeGroupId(m_nativeGroupId);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
std::string
Group::NativeGroupId::toString() const
{
    std::ostringstream oStream;
    oStream << m_nativeGroupId;
    return oStream.str();
}

