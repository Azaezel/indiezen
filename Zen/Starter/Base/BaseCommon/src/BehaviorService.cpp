//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
BehaviorService::pGameObjectBehaviors_type
BehaviorService::createBehaviors(const std::string& _name)
{
    // Make sure this isn't a duplicate behavior
    BehaviorsMap_type::iterator iter = m_behaviorsMap.find(_name);
    if (iter == m_behaviorsMap.end())
    {
        GameObjectBehaviors* pBehaviors = new GameObjectBehaviors;
        m_behaviorsMap[_name] = pBehaviors;
        return pBehaviors;
    }
    else
    {
        return iter->second;
    }
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
BehaviorService::pGameObjectBehaviors_type
BehaviorService::createBehaviors(const std::string& _name, pGameObjectBehaviors_type _pTemplate)
{
    // Make sure this isn't a duplicate behavior
    BehaviorsMap_type::iterator iter = m_behaviorsMap.find(_name);
    if (iter == m_behaviorsMap.end())
    {
        GameObjectBehaviors* pBehaviors = new GameObjectBehaviors(dynamic_cast<GameObjectBehaviors*>(_pTemplate));
        m_behaviorsMap[_name] = pBehaviors;
        return pBehaviors;
    }
    else
    {
        return iter->second;
    }
}

