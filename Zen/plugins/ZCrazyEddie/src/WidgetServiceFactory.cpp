{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
WidgetServiceFactory::~WidgetServiceFactory()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
WidgetServiceFactory&
WidgetServiceFactory::getSingleton()
{
    static WidgetServiceFactory factory;
    return factory;
}

WidgetServiceFactory::pWidgetService_type
WidgetServiceFactory::create(const std::string& _type, config_type& _config)
{
    // TODO Possibly pass _config to the widget service
    WidgetService* pRawService = new WidgetService();

    pWidgetService_type pService(
        pRawService,
        boost::bind(
            &WidgetServiceFactory::destroy,
            this,
            _1
        )
    );

    wpWidgetService_type pWeakPtr(pService);
    
    pRawService->setSelfReference(pWeakPtr);

    return pService;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
WidgetServiceFactory::destroy(wpWidgetService_type _pWidgetService)
{
    WidgetService* pWidgetService = dynamic_cast<WidgetService*>(_pWidgetService.get());

    if (pWidgetService)
    {
        delete pWidgetService;
    }
    else
    {
        // TODO Error
    }
}

