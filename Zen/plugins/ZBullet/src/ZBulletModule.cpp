//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

class ZBulletModule
:   public Plugins::I_Module
{
    /// @name Types
    /// @{
public:
    typedef boost::shared_ptr<ZBulletPlugin>     plugin_ptr_type;
    /// @}

    /// @name I_Module implementation
    /// @{
public:
    virtual plugin_name_collection_type& getPluginNames();
    virtual Plugins::I_Module::plugin_ptr_type getPlugin(const plugin_name_type& _pluginName);
    virtual void destroyPlugin(Plugins::I_Module::plugin_ptr_type _plugin);
    /// @}

    /// @name 'Structors
    /// @{
public:
             ZBulletModule();
    virtual ~ZBulletModule();
    /// @}

    /// @name Implementation
    /// @{
public:
    static ZBulletModule& instance();
    /// @}

    /// @name Member Variables
    /// @{
private:
    plugin_name_collection_type                 m_names;
    plugin_ptr_type                             m_pPlugin;
    /// @}

};  // class ZBulletModule

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ZBulletModule::ZBulletModule()
{
    // TODO: Register the plugins
    m_pPlugin = plugin_ptr_type(new ZBulletPlugin);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ZBulletModule::~ZBulletModule()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static ZBulletModule* pModule = NULL;

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ZBulletModule& 
ZBulletModule::instance()
{
    // TODO: Guard
    if (pModule == NULL)
    {
        pModule = new ZBulletModule();
    }

    return *pModule;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Zen::Plugins::I_Module::plugin_name_collection_type& 
ZBulletModule::getPluginNames()
{
    return m_names;
}

