//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
std::string
DataElement::getDBString() const
{
    std::stringstream stream;

    stream << m_pElement->getDBString();

    if (getUnderlyingType() == DATETIME)
    {
        stream << "::TIMESTAMP";
    }

    return stream.str();
}

