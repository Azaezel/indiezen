#include "BasicCommandHandler.hpp"

#include <Zen/Engine/Server/I_ConsoleStream.hpp>

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
namespace Zen {
namespace ZBasicCommandLineParser {
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
BasicCommandHandler::BasicCommandHandler(const std::string& _command, function_type _function, const std::string& _usage)
:   m_command(_command)
,   m_function(_function)
,   m_usage(_usage)
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
BasicCommandHandler::~BasicCommandHandler()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string&
BasicCommandHandler::getCommand() const
{
    return this->m_command;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
BasicCommandHandler::parameterList_type
BasicCommandHandler::getParameters() const
{
    return this->m_parameterList;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string&
BasicCommandHandler::getUsage() const
{
    return this->m_usage;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
BasicCommandHandler::handleCommand(iterator_type _start, const iterator_type _end)
{
    // Starting a new command, wipe out parms
    this->m_parameterList.clear();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
BasicCommandHandler::handleParameter(iterator_type _start, const iterator_type _end)
{
    // Push the parameter onto the parameter list
    std::string param(_start, _end);
    this->m_parameterList.push_back(std::string(_start, _end));
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
BasicCommandHandler::done(iterator_type _start, const iterator_type _end, const pConsoleStream_type* _pConsoleStream)
{
    **_pConsoleStream << this->execute(*_pConsoleStream);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
std::string
BasicCommandHandler::execute(const pConsoleStream_type& _pConsoleStream)
{
    return m_function(_pConsoleStream, m_parameterList);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
}   // namespace ZBasicCommandLineParser
}   // namespace Zen
