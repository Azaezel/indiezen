//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
/// Reformat an integer to the big-endian network format
boost::uint32_t
native_to_big(boost::uint32_t x)
{
    boost::uint32_t  temp;
    unsigned char* tp = reinterpret_cast<unsigned char *>( &temp );

    for (std::size_t i = sizeof(x); i > 0; --i)
    {
        tp[i - 1] = static_cast<unsigned char>(x);
        x >>= CHAR_BIT;
    }

    return temp;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
/// Restore an integer from the big-endian network format
boost::uint32_t
big_to_native(boost::uint32_t x)
{
    boost::uint32_t  temp = 0;
    unsigned char* xp = reinterpret_cast<unsigned char*>(&x);

    for (std::size_t i = 0; i < sizeof(x); ++i)
    {
        temp <<= CHAR_BIT;
        temp |= xp[i];
    }

    return temp;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
bool
MessageBuffer::decodeHeader()
{
    m_bodyLength = big_to_native(m_bodyLength);
    if (m_bodyLength > MAX_BODY_LENGTH)
    {
        // TODO Error
        m_bodyLength = 0;
        return false;
    }
    return true;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
MessageBuffer::encodeHeader()
{
    m_bodyLength = native_to_big(m_bodyLength);
}

