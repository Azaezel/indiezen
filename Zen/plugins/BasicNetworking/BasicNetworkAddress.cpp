//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
std::string
BasicNetworkAddress::asString() const
{
    std::ostringstream buffer;

    buffer << inet_ntoa(m_sockaddr.sin_addr) << ntohs(m_sockaddr.sin_port);

    return buffer.str();
}

