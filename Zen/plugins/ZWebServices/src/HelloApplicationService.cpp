//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
HelloApplicationService::handleRequest(pRequest_type _pRequest, pResponseHandler_type _pResponseHandler)
{
    // This should be a web request coming from the client

    I_WebRequest* pRequest = dynamic_cast<I_WebRequest*>(_pRequest.get());

    if (pRequest)
    {
        I_WebResponse::pResponse_type pResponse =
        I_WebResponse::createResponse(pRequest->getSourceEndpoint(), I_WebResponse::ok, 
            "<html>"
            "<head><title>Hello, World!</title></head>"
            "<body><h1>Zen Enterprise Web Services says \"Hi!\"</h1></body>"
            "</html>"
            );

        _pResponseHandler->handleResponse(pResponse);
    }
    else
    {
        // TODO Error!
    }
}

