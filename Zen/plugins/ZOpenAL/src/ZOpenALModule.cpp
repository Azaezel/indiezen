//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ZOpenALModule& 
ZOpenALModule::instance()
{
    // TODO: Guard
    if (pModule == NULL)
    {
        pModule = new ZOpenALModule();
    }

    return *pModule;
}

