class GLRendererModule
:   public Plugins::I_Module
{
    /// @name Types
    /// @{
public:
    typedef boost::shared_ptr<GLRendererModule>         plugin_ptr_type;
    /// @}

    /// @name I_Module implementation
    /// @{
public:
    virtual plugin_name_collection_type& getPluginNames();
    virtual Plugins::I_Module::plugin_ptr_type getPlugin(const plugin_name_type& _pluginName);
    virtual void destroyPlugin(Plugins::I_Module::plugin_ptr_type _plugin);
    /// @}

    /// @name 'Structors
    /// @{
public:
             GLRendererModule();
    virtual ~GLRendererModule();
    /// @}

    /// @name Implementation
    /// @}
public:
    static GLRendererModule& instance();
    /// @}

    /// @name Member Variables
    /// @{
private:
    plugin_name_collection_type                 m_names;
    plugin_ptr_type                             m_pPlugin;
    /// @}
};  // class GLRendererModule

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
GLRendererModule::GLRendererModule()
{
    // TODO: Register the plugins
    m_pPlugin = plugin_ptr_type(new GLRendererPlugin);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
GLRendererModule::~GLRendererModule()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static GLRendererModule* pModule = NULL;

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
GLRendererModule& 
GLRendererModule::instance()
{
    // TODO: Guard
    if (pModule == NULL)
    {
        pModule = new GLRendererModule();
    }

    return *pModule;
}

