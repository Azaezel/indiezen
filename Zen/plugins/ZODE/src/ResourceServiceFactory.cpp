//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ResourceServiceFactory::ResourceServiceFactory()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ResourceServiceFactory::~ResourceServiceFactory()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static ResourceServiceFactory sm_resourceServiceFactory;

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ResourceServiceFactory&
ResourceServiceFactory::instance()
{
    return sm_resourceServiceFactory;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Engine::Resource::I_ResourceServiceFactory::pResourceService_type
ResourceServiceFactory::create(const std::string& _type)
{
    // TODO Verify that _type is "ZODE"

    // TODO Guard
    if (m_pResourceService == NULL)
    {
        PhysicsResourceService* pRawService = new PhysicsResourceService();

        pResourceService_type pService(pRawService, boost::bind(&ResourceServiceFactory::destroy, this, _1));

        wpResourceService_type pWeakPtr(pService);

        pRawService->setSelfReference(pWeakPtr);

        m_pResourceService = pService;
    }

    return m_pResourceService;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
ResourceServiceFactory::destroy(wpResourceService_type _pService)
{
    /// Fire the service's onDestroyEvent.
    _pService->onDestroyEvent(_pService);

    /// Delete the service
    PhysicsResourceService* pService = dynamic_cast<PhysicsResourceService*>(_pService.get());

    if (pService != NULL)
    {
        delete pService;
    }
    else
    {
        // TODO Error!
    }
}

