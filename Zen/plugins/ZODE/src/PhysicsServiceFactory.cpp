PhysicsServiceFactory::PhysicsServiceFactory()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
PhysicsServiceFactory::~PhysicsServiceFactory()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static PhysicsServiceFactory sm_physicsServiceFactory;

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
PhysicsServiceFactory&
PhysicsServiceFactory::getSingleton()
{
    return sm_physicsServiceFactory;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
PhysicsServiceFactory::pPhysicsService_type
PhysicsServiceFactory::create(const std::string& _type, Engine::Physics::I_PhysicsServiceFactory::config_type& _config)
{
    std::cout << "PhysicsServiceFactory::create" << std::endl;

    PhysicsService* pRawService = new PhysicsService();

	PhysicsServiceFactory::pPhysicsService_type pService(pRawService, boost::bind(&PhysicsServiceFactory::destroy, this, _1));

    wpPhysicsService_type pWeakPtr(pService);

    pRawService->setSelfReference(pWeakPtr);

    assert(pService.get() != NULL);

    return pService;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
PhysicsServiceFactory::destroy(wpPhysicsService_type _pService)
{
    /// Fire the service's onDestroyEvent.
    _pService->onDestroyEvent(_pService);

    /// Delete the service
    PhysicsService* pService = dynamic_cast<PhysicsService*>(_pService.get());

    if (pService != NULL)
    {
        delete pService;
    }
    else
    {
        // TODO Error!
    }
}

