//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

class ZInputModule
:   public Plugins::I_Module
{
    /// @name Types
    /// @{
public:
    typedef boost::shared_ptr<ZInputPlugin>     plugin_ptr_type;
    /// @}

    /// @name I_Module implementation
    /// @{
public:
    virtual plugin_name_collection_type& getPluginNames();
    virtual Plugins::I_Module::plugin_ptr_type getPlugin(const plugin_name_type& _pluginName);
    virtual void destroyPlugin(Plugins::I_Module::plugin_ptr_type _plugin);
    /// @}

    /// @name 'Structors
    /// @{
public:
             ZInputModule();
    virtual ~ZInputModule();
    /// @}

    /// @name Implementation
    /// @}
public:
    static ZInputModule& instance();
    /// @}

    /// @name Member Variables
    /// @{
private:
    plugin_name_collection_type                 m_names;
    plugin_ptr_type                             m_pPlugin;
    /// @}

};  // class ZInputModule

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ZInputModule::ZInputModule()
{
    // TODO: Register the plugins
    m_pPlugin = plugin_ptr_type(new ZInputPlugin);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ZInputModule::~ZInputModule()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static ZInputModule* pModule = NULL;

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ZInputModule& 
ZInputModule::instance()
{
    // TODO: Guard
    if (pModule == NULL)
    {
        pModule = new ZInputModule();
    }

    return *pModule;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Zen::Plugins::I_Module::plugin_name_collection_type& 
ZInputModule::getPluginNames()
{
    return m_names;
}

