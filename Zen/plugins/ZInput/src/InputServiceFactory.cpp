//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
InputServiceFactory&
InputServiceFactory::getSingleton()
{
    return sm_serviceFactory;
}
