//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
WaterServiceFactory::WaterServiceFactory()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
WaterServiceFactory::~WaterServiceFactory()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static WaterServiceFactory sm_waterServiceFactory;

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
WaterServiceFactory&
WaterServiceFactory::instance()
{
    return sm_waterServiceFactory;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
WaterServiceFactory::pWaterService_type
WaterServiceFactory::create(const std::string& _type, Engine::World::I_WaterServiceFactory::config_type& _config)
{
    WaterService* pRawService = new WaterService();

	WaterServiceFactory::pWaterService_type pService(pRawService, boost::bind(&WaterServiceFactory::destroy, this, _1));

    wpWaterService_type pWeakPtr(pService);

    pRawService->setSelfReference(pWeakPtr);

    return pService;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
WaterServiceFactory::destroy(wpWaterService_type _pService)
{
    /// Fire the service's onDestroyEvent.
    _pService->onDestroyEvent(_pService);

    /// Delete the service
    WaterService* pService = dynamic_cast<WaterService*>(_pService.get());

    if (pService != NULL)
    {
        delete pService;
    }
    else
    {
        // TODO Error!
    }
}

