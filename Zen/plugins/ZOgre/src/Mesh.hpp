//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
;

/// Mesh implementation.
/// A Mesh is a light wrapper around an Ogre::MeshPtr.
class Mesh
:   public Zen::Engine::Rendering::I_Mesh
{
    /// @name Types
    /// @{
public:
    /// @}

    /// @name I_Mesh implementation
    /// @{
public:
    virtual void getMesh(std::vector<Zen::Math::Point3>& _vertices, std::vector<boost::array<int, 3> >& _indices);
    /// @}

    /// @name I_ScriptableType implementation
    /// @{
public:
    virtual Scripting::I_ObjectReference* getScriptObject();
    /// @}

    /// @name 'Structors
    /// @{
public:
             Mesh(Ogre::MeshPtr _pMesh);
    virtual ~Mesh();
    /// @}

    /// @name Member Variables
    /// @{
private:
    ScriptObjectReference_type*     m_pScriptObject;

    Ogre::MeshPtr                   m_pMesh;
    /// @}

};  // class Mesh

