//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void 
AnimationState::copyStateFrom(const I_AnimationState& _state)
{
    const AnimationState* pAnimationState =
        dynamic_cast<const AnimationState*>(&_state);

    if( pAnimationState != NULL )
    {
        m_pAnimationState->copyStateFrom(pAnimationState->getOgreAnimationState());
    }
    else
    {
        throw Zen::Utility::runtime_exception("AnimationState::copyStateFrom() : Invalid type.");
    }
}

