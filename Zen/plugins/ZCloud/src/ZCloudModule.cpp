//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

class ZCloudModule
:   public Plugins::I_Module
{
    /// @name Types
    /// @{
public:
    typedef boost::shared_ptr<ZCloudPlugin>     plugin_ptr_type;
    /// @}

    /// @name I_Module implementation
    /// @{
public:
    virtual plugin_name_collection_type& getPluginNames();
    virtual Plugins::I_Module::plugin_ptr_type getPlugin(const plugin_name_type& _pluginName);
    virtual void destroyPlugin(Plugins::I_Module::plugin_ptr_type _plugin);
    /// @}

    /// @name 'Structors
    /// @{
public:
             ZCloudModule();
    virtual ~ZCloudModule();
    /// @}

    /// @name Implementation
    /// @{
public:
    static ZCloudModule& instance();
    /// @}

    /// @name Member Variables
    /// @{
private:
    plugin_name_collection_type                 m_names;
    plugin_ptr_type                             m_pPlugin;
    /// @}

};  // class ZFMODModule

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ZCloudModule::ZCloudModule()
{
    // TODO: Register the plugins
    m_pPlugin = plugin_ptr_type(new ZCloudPlugin);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ZCloudModule::~ZCloudModule()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static ZCloudModule* pModule = NULL;

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ZCloudModule& 
ZCloudModule::instance()
{
    // TODO: Guard
    if (pModule == NULL)
    {
        pModule = new ZCloudModule();
    }

    return *pModule;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Zen::Plugins::I_Module::plugin_name_collection_type& 
ZCloudModule::getPluginNames()
{
    return m_names;
}

