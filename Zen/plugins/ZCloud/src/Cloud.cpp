//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Cloud::Cloud()
{
    this->m_pMutex = Zen::Threading::MutexFactory::create();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Cloud::~Cloud()
{
    Zen::Threading::MutexFactory::destroy(this->m_pMutex);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Cloud::addParticle(pCloudParticle_type& _pCloudParticle)
{
    Zen::Threading::CriticalSection guard(this->m_pMutex);

    std::set<pCloudParticle_type>::iterator iter = 
        this->m_particles.find(_pCloudParticle);

    if( iter == this->m_particles.end() )
    {
        this->m_particles.insert(_pCloudParticle);
    }
    else
    {
        throw std::exception("Zen::ZCloud::Cloud::addParticle() : Particle is already a member of the set.");
    }
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Cloud::dropParticle(pCloudParticle_type& _pCloudParticle)
{
    Zen::Threading::CriticalSection guard(this->m_pMutex);

    std::set<pCloudParticle_type>::iterator iter =
        this->m_particles.find(_pCloudParticle);

    if( iter != this->m_particles.end() )
    {
        this->m_particles.erase(iter);
    }
    else
    {
        throw std::exception("Zen::ZCloud::Cloud::dropParticle() : Particle is not a member of the set.");
    }
}

